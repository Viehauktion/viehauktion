<p><u>Kurze Erläuterung:</u><br />
  Bei der &bdquo;Schnitt-Verkaufsmaske&ldquo; ist für das Erreichen des vollen Basispreises ausschließlich die Einhaltung eines Schlachtgewichtkorridors zwischen 84 und 106 kg notwendig. In diesem Bereich sind keine weiteren Parameter für die Bezahlung ausschlaggebend. Außerhalb dieses Korridors wird nach obiger FOM- Maske klassifiziert, will heißen, dass das Schlachtgewicht, das Gewicht des Schinkens, sowie der Muskelfleischanteil maßgebend für die Auszahlung je kg Schlachtgewicht sind.</p>
